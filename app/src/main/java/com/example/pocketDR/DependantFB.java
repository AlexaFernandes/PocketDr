package com.example.pocketDR;

public class DependantFB {
    private String uid;
    private String name;

    public DependantFB() {
        uid ="";
        name ="";
    }


    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
